package poo.Ejercicio44;
class paciente{
    String nombre;
    int edad;

    public paciente(String nombre, int edad) {
        this.nombre = nombre;
        this.edad = edad;
    }
}
// Crear un paciente y instanciarlo se relacionan pero no son exactamente lo mismo.
//  Crear un paciente se refiere al proceso de definir la clase paciente y sus atributos,
//  mientras que instanciarlo se refiere a crear un objeto específico de esa clase con valores concretos para sus atributos. 
// En otras palabras, crear un paciente es el diseño general, mientras que instanciarlo es la acción de generar una instancia concreta de ese diseño.